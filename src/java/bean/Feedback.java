public class Feedback
{
    private int fid;
    private String fusr;
    private String fsub;
    private String ftext;

    public int getFid() {
        return fid;
    }

    public void setFid(int fid) {
        this.fid = fid;
    }

    public String getFusr() {
        return fusr;
    }

    public void setFusr(String fusr) {
        this.fusr = fusr;
    }

    public String getFsub() {
        return fsub;
    }

    public void setFsub(String fsub) {
        this.fsub = fsub;
    }

    public String getFtext() {
        return ftext;
    }

    public void setFtext(String ftext) {
        this.ftext = ftext;
    }
    
    
}
